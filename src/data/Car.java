package data;

public class Car {
    public Car(int price, int year, int weight, String color, String producer){
        this.price = price;
        this.year = year;
        this.weight = weight;
        this.color = color;
        this.producer = producer;
    }

    public int price;
    public int year;
    public int weight;
    public String color;
    public String producer;
}
